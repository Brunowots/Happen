	local playerId = player:getId()

	if not player:isVip() then
		npcHandler:say('Foi mal, compro apenas de player VIP, compre a sua na gamestore.', npc, creature)
		return false
	end
	
	if player:getLevel() < 100 then
		npcHandler:say('Foi mal, somente nivel 100+', npc, creature)
		return false
	end

	return true
end

